#ifndef MODULE_AUDIO_H
#define MODULE_AUDIO_H

#include "config.h"

// ============================================================================
// МОДУЛЬ АУДИО: Управление звуками бузера
// ============================================================================
// Минималистичный модуль без библиотек. Использует встроенный tone() API.
// Размер: ~2KB, без динамической памяти.
// ============================================================================

namespace Audio {

    // ========================================================================
    // BOOT SEQUENCE: Гул трансформатора при включении (Vault-Tec style)
    // ========================================================================
    // Эффект: Низкий гул + подъем частоты (имитация включения системы)
    // Воспроизводится: 1 раз при старте (в setup())
    // Длительность: ~300-350 мс
    inline void playBootSound() {
        // Фаза 1: Начальный гул (120 → 90 Гц)
        tone(BUZZER_PIN, 120);
        delay(8);
        noTone(BUZZER_PIN);
        delay(15);
        
        tone(BUZZER_PIN, 90);
        delay(12);
        noTone(BUZZER_PIN);
        delay(100);
        
        // Фаза 2: Нарастание частоты (100 → 320 Гц) - имитация "просыпания" системы
        for (int freq = 100; freq < 320; freq += 3) {
            tone(BUZZER_PIN, freq);
            delay(6);
        }
        
        // Фаза 3: Финальный сигнал (320 Гц, долгий)
        tone(BUZZER_PIN, 320);
        delay(150);
        noTone(BUZZER_PIN);
    }

    // ========================================================================
    // BEEP SWITCH: Звук переключения экранов
    // ========================================================================
    // Частота: 2500 Гц (оптимальная для пьезобузера)
    // Длительность: 30 мс
    // Громкость: Встроенная в tone() (оптимально)
    // Используется: module_buttons.cpp строка 147
    inline void playBeepSwitch() {
        tone(BUZZER_PIN, 2500, 30);
    }

    // ========================================================================
    // BEEP AUTO MODE: Звук возврата в автоматический режим
    // ========================================================================
    // Частота: 2800 Гц (немного выше для "поднятия тона")
    // Длительность: 25 мс
    // Характер: Одиночный легкий клик
    // Используется: module_buttons.cpp строка 212
    inline void playBeepAutoMode() {
        tone(BUZZER_PIN, 2800, 25);
    }

    // ========================================================================
    // BEEP MANUAL MODE: Звук активации ручного режима
    // ========================================================================
    // Частота: 2500 Гц (основной тон)
    // Паттерн: Двойной клик (подтверждение выбора)
    // Длительность: 20 мс + пауза 40 мс + 20 мс
    // Характер: Решительный, уверенный звук
    // Используется: module_buttons.cpp строка 225-227
    inline void playBeepManualMode() {
        tone(BUZZER_PIN, 2500, 20);
        delay(40);
        tone(BUZZER_PIN, 2500, 20);
    }

    // ========================================================================
    // BEEP CUSTOM: Универсальный beep для будущих нужд
    // ========================================================================
    // Параметры:
    //   freq: Частота в Гц (рекомендуется 1500-3500)
    //   duration_ms: Длительность в миллисекундах
    // 
    // Примеры:
    //   playBeepCustom(2500, 50);  // Долгий beep
    //   playBeepCustom(1000, 100); // Низкий долгий сигнал (ошибка)
    inline void playBeepCustom(int freq, int duration_ms) {
        tone(BUZZER_PIN, freq, duration_ms);
    }

} // namespace Audio

#endif // MODULE_AUDIO_H
